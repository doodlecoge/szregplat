public class Ws320RuntimeException extends RuntimeException {
    public Ws320RuntimeException() {
        super();
    }

    public Ws320RuntimeException(String message) {
        super(message);
    }

    public Ws320RuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public Ws320RuntimeException(Throwable cause) {
        super(cause);
    }
}
